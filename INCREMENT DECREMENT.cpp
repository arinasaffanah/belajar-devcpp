#include<iostream>
using namespace std;

int main(){
	//Increment decrement -> digunakan dalam kelilpatan -> untuk lompatan
	int x,y;
	
	//post
	x = 8;
	cout<<x++<<endl;
	cout<<x++<<endl;
	cout<<x++<<endl;
	cout<<x++<<endl;
	cout<<"-------------"<<endl;
	
	//pre
	y = 8;
	cout<<++y<<endl;
	cout<<++y<<endl;
	cout<<++y<<endl;
	cout<<++y<<endl;

	cout<<"============="<<endl;	
	
	// decrement --
	
	
	//post
	x = 8;
	cout<<x--<<endl;
	cout<<x--<<endl;
	cout<<x--<<endl;
	cout<<x--<<endl;
	cout<<"-------------"<<endl;
	
	//pre
	y = 8;
	cout<<--y<<endl;
	cout<<--y<<endl;
	cout<<--y<<endl;
	cout<<--y<<endl;
	
}
